const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('lang') || 'ur')
  const dir = lang === 'ur' ? 'rtl' : 'ltr'

  useEffect(() => {
    localStorage.setItem('lang', lang)
    document.documentElement.lang = lang
    document.documentElement.dir = dir
  }, [lang, dir])

  const t = (key) =>
    (translations[lang] && translations[lang][key]) || translations.en[key] || key
  const toggle = () => setLang((l) => (l === 'ur' ? 'en' : 'ur'))

  return (
    <I18nContext.Provider value={{ lang, dir, t, toggle, setLang }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useT() {
  const ctx = useContext(I18nContext)
  if (!ctx) return { t: (k) => k, lang: 'en', dir: 'ltr', toggle: () => {}, setLang: () => {} }
  return ctx
}
